/* Comparison operators
 ** allow us to compare values and return true or false
 ** here are the operators : < <= == != > >= 
 */

//some comparisons
console.log(10 == 10); //return true

console.log(10 < 10);

console.log(10 <= 10);

console.log(10 > 10);

console.log(10 >= 10);

console.log(10 != 10);

console.log("A" == "B"); //return false

console.log("A" != "B");





